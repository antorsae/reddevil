G_SINUS	LEA SINUS,A0
	MOVE.W #MAXX-1,D7
GENEY	MOVEQ #0,D0
	MOVE.W (A0),D0
	MULU #160,D0
	MOVE.L D0,(A0)+
	MOVE.L #0,(A0)+
	DBF D7,GENEY
	MOVE.W #MAXY-1,D7
GENEX	MOVEQ #0,D0
	MOVEQ #0,D1
	MOVE.W (A0),D0
	DIVU #16,D0
	MOVE.W D0,D1
	MULU #8,D1
	CLR.W D0
	SWAP D0
	MULU #2*4*16*2,D0
	ADD.L #SPR_OR,D0
	ADD.L #$F8000,D1
	MOVE.L D0,(A0)+
	MOVE.L D1,(A0)+
	DBF D7,GENEX
	RTS
GET_N	MOVE.W SUX,D7
	CMP.W #MAXY*8,D7
	BLT.S NOOVX
	SUB.W #MAXY*8,D7
	MOVE.W D7,SUX
NOOVX	LEA SINUS+(MAXX*8),A2
	MOVE.L 0(A2,D7.W),A0
	MOVE.L 4(A2,D7.W),A1
	MOVE.W SUY,D7
	CMP.W #MAXX*8,D7
	BLT.S NOOVY
	SUB.W #MAXX*8,D7
	MOVE.W D7,SUY
NOOVY	LEA SINUS(PC,D7.W),A2
	ADDA.L (A2),A1
	RTS
GET_NA	MOVE.W DUX,D7
	CMP.W #MAXY*8,D7
	BLT.S NOINX  
	SUB.W #MAXY*8,D7
	MOVE.W D7,DUX
NOINX	LEA SINUS+(MAXX*8),A2
	MOVE.L 0(A2,D7.W),A0
	MOVE.L 4(A2,D7.W),A1
	MOVE.W DUY,D7
	CMP.W #MAXX*8,D7
	BLT.S NOINY  
	SUB.W #MAXX*8,D7
	MOVE.W D7,DUY
NOINY	LEA SINUS(PC,D7.W),A2
	ADDA.L (A2),A1
	RTS
