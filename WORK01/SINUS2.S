	LEA $F8000+(17*160),A0
	BSR ROX_AND
	DBF D7,ROT_OR
	RTS
C_SP	MOVEQ #16-1,D7
CPSLOOP	MOVE.L (A0),(A1)
	MOVE.L 4(A0),4(A1)
	MOVE.L 8(A0),8(A1)
	MOVE.L 8+4(A0),8+4(A1)
	LEA 160(A0),A0
	LEA 160(A1),A1
	DBF D7,CPSLOOP
	RTS
