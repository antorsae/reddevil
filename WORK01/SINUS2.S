	TRAP #1
	LEA 4(A7),A7
	SUB.B #'1',D0
	BNE.S INCXX
	ADDQ.W #8,INCX
INCXX	CMP.B #1,D0
	BNE.S DECXX
	SUBQ.W #8,INCX
DECXX	CMP.B #2,D0
	BNE.S INCYY
	ADDQ.W #8,INCY
INCYY	CMP.B #3,D0
	BNE.S DECYY
	SUBQ.W #8,INCY
DECYY	CMP.B #4,D0
	BNE.S DEXX
	ADDQ.W #8,DOX
DEXX	CMP.B #5,D0
	BNE.S INDY
	SUBQ.W #8,DOX
INDY	CMP.B #6,D0
	BNE.S DEDY
	ADDQ.W #8,DOY
DEDY	CMP.B #7,D0
	BNE.S DEDD
	SUBQ.W #8,DOY
DEDD	RTS
PUT_BS	MOVE.L INCX,D0
	ADD.L D0,SUX
	MOVE.L SUX,DUX
	BSR GET_N
	MOVE.L A1,DAT_BO
	BSR P_SPR
	LEA DAT_BO+4,A3
	MOVE.L DOX,D2
	REPT BOLAS
	BSR GET_NA
	MOVE.L A1,(A3)+
	BSR P_SPR
	ADD.L D2,DUX
	ENDR
	RTS
BORRA_B	LEA DAT_BO,A0
	MOVE.W #BOLAS,D0
	MOVEQ #0,D7
